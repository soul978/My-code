public class nitwise {
    public static void main(String[] args) {
       int a= 9,b = 10;
       System.out.println(a|b);
       System.out.println(a&b);
       System.out.println(a^b);

       System.out.println();
       System.out.println(a<<1);
       System.out.println(a<<2);
       System.out.println(a<<3);

       System.out.println(b>>1);
       System.out.println(b>>2);
    }
}
