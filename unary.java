public class unary {
    public static void main(String[] args) {
        int a = 5,b = 5;
        System.out.println(a++);
        System.out.println(a);
System.out.println();
        System.out.println(++b);
        System.out.println(b);
System.out.println();
        int x = a++;
        int y = ++b;

        System.out.println(x);
        System.out.println(y);
System.out.println();
        System.out.println(a);
        System.out.println(b);
     
        
    }
}
